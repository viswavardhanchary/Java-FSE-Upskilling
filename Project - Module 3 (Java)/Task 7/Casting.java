public class Casting {
    public static void main(String[] args) {
       double b = 10.6;
       int cb = (int)10.6;
       int a = 10;
       double ca = a;
       System.out.println("Converted double " + b + " to Integer " + cb);
       System.out.println("Converted Integer " + a + " to Double " + ca);
       
    }
}
